class SwitchCaseDemo{

	public static void main( String args[]){

		int choice = 2;

		switch( choice ){

			case 1:
				System.out.println( "case 1");
				break;
			case 2:
                                System.out.println( "case 2");
                                break;
			case 3:
                                System.out.println( "case 3");
                                break;
			default:
				System.out.println( "default case");
		}

		System.out.println( "Out of switch case");
	}

}
